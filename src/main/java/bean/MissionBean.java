package bean;

public class MissionBean {
	private int NUM;
	private String SUBJECT;
	
	
	public MissionBean() {	}


	public int getNUM() {
		return NUM;
	}


	public void setNUM(int nUM) {
		NUM = nUM;
	}


	public String getSUBJECT() {
		return SUBJECT;
	}


	public void setSUBJECT(String sUBJECT) {
		SUBJECT = sUBJECT;
	}
	
	

}
